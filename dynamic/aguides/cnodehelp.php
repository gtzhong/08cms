<li><a name="01"></a><b>类目结构概述</b>
<ul type="1">
<li><a name="01_01"></a>多重类目体系<br>08CMS使用多重×多层自由类目体系，网站管理员根据网站需要自由建立多重类别体系，在每个类别体系下，自由建立多层类目，所以每个文档可以同时拥有不同分类体系的类目属性，用户可以从不同的角度来检索需要的信息，文档的分类定义也更加精确有效。
