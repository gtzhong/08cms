<li><a name="02"></a><b>多重类目的应用样例</b>：以中学生学习网站为例来简单说明多重类目的应用。
<ul type="1">
<li><a name="02_01"></a>建立分类体系及体系内分类（仅以单层类目为例）
<ul>
<li>栏目（系统内置）：资讯、课件、题库、视频、下载等。
<li>学科类系（自定）：语文、数学、英语、物理、化学、历史等。
<li>年级类系（自定）：初一、初二、初三、高一、高二、高三、综合等。
</ul>
<li><a name="02_02"></a>类目页面节点：单个类目或不同类系的类目组合均可成为类目节点。类目节点允许生成相应的类目页面，类目节点结构用于配置系统需要生成哪些类目节点。
<ul>
<li>一级节点，如：资讯、课件、语文、数学、初一、初二等。
<li>二级节点，如：资讯->语文、资讯->数学、资讯->初一、资讯->初二、语文->初一、语文->初二等。
<li>三级节点，如：资讯->语文->初一、语文->初一->题库、高一->视频->英语等。
</ul>
<li><a name="02_03"></a>类目节点的功能&特性
<ul>
<li>类目页面：只有生成了类目节点才允许生成相应的类目页面，类系中类目定义不会自动生成节点，所有节点生成需要通过类目节点配置。
<li>类目链接：以语文、数学、英语、物理、化学、历史的类目列表为例，其中"语文"只是类目显示项，它可能还隐含其它类系的类目属性，所以其链接并不仅仅允许指向"语文"节点，而是结合模板定义，还有可能是"资讯->语文"、"高一->语文"、"资讯->高一->语文"等等的节点。
<li>方向性：节点具有方向性，代表内容索引的方向。如"资讯->语文"与"语文->资讯"属于不同的节点，指向不同的页面。
<li>自主配置：自由选择类目的一个或多个类目参与节点生成，如果初一没有物理科目，则不要生成"初一->物理"或"物理->初一"及与此相类的节点。
</ul>
<li>类目相关的术语
<ul>
<li>节点成员：可用于构建类目节点的类系，栏目内定为节点成员，其它类系自定，本例中的类系均被认为是节点成员。
<li>频道类系与频道：在节点组成中位于首层的类系为频道类系，如：语文->初一->题库，学科类系为频道类系，语文则为其中一个具体频道。
</ul>
</ul>
