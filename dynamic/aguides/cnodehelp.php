<li><a name="03"></a><b>多重类目体系</b>
<ul type="1">
<li><a name="03_01"></a>类别体系：以某种标准来分类的类目集合，类别体系由网站管理者自定义生成，允许多个分类体系。
<li><a name="03_02"></a>栏目：系统内置的分类体系。
<li><a name="03_03"></a>类系的特征
<ul>
<li>管理专用：只能在管理后台进行设置的类系。管理类系可用作网站的管理手段，如精品、推荐等。
<li>节点成员：可参与组成类目页面节点的类系。
<li>频道类系：可作为类目页面节点的首重分类的类系，频道类系首先必须是节点成员。
</ul>
<li><a name="03_04"></a>类系中的分类
<ul>
<li>多层分类，由网站管理者自定义。
<li>允许多种模型的文档共存于同一分类。
<li>可为分类绑定域名。
<li>允许设定权限与奖励，类目设定的内容权限对类目内的文档有效，但不会作用于子类目。
</ul>
</ul>
